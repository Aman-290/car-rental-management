        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) RCTable.getModel();
        model.setRowCount(0);

        // Get the search string from the text field
        String searchText = searchtxt.getText().trim().toLowerCase();

        // Load data from the CSV file and add matching rows to the table
        try {
            BufferedReader reader = new BufferedReader(new FileReader("C:\\Users\\amans\\Documents\\code\\java project data\\details.csv"));
            String line;
            int k = 0;
            while ((line = reader.readLine()) != null) {
                String[] rowData = line.split(",");
                if (rowData[0].toLowerCase().equals(searchText)) {
                    model.addRow(rowData);
                    k=1;
                    
                }
                else if(searchtxt.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "Enter a value");
                    k=2;
                    break;
                }

            }
                    if(k!=1 && k !=2){
                    JOptionPane.showMessageDialog(null, "No records found");
                }
            reader.close();
        } catch (IOException ex) {
            ex.printStackTrace();
        }

