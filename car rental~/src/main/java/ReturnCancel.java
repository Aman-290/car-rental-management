   public void clearAndLoadTable(JTable table, String filename) {
    DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.setRowCount(0); // Remove all rows
    try {
        BufferedReader reader = new BufferedReader(new FileReader(filename));
        String line;
        while ((line = reader.readLine()) != null) {
            String[] rowData = line.split(",");
            model.addRow(rowData);
        }
        reader.close();
    } catch (IOException e) {
        e.printStackTrace();
    }
            ProceedButton.setEnabled(false);
            ProceedButton.setBackground(new Color(255, 255, 255));
            
}
   
   public int findRowByValue(JTable table, String columnName, Object value) {
    DefaultTableModel model = (DefaultTableModel) table.getModel();
    int rowCount = model.getRowCount();
    int column = model.findColumn(columnName);
    for (int i = 0; i < rowCount; i++) {
        if (model.getValueAt(i, column).equals(value)) {
            return i;
        }
    }
    return -1; // Return -1 if the row is not found
}
   
   private void deleteRowFromCSV(String csvFilePath, JTable table) {
    int selectedRow = table.getSelectedRow();
    String selectedValue = table.getValueAt(selectedRow, 0).toString(); // get the value of the first column in the selected row

    try {
        File inputFile = new File(csvFilePath);
        File tempFile = new File("temp.csv");

        BufferedReader reader = new BufferedReader(new FileReader(inputFile));
        BufferedWriter writer = new BufferedWriter(new FileWriter(tempFile));

        String currentLine;
        while ((currentLine = reader.readLine()) != null) {
            String[] row = currentLine.split(",");
            if (!row[0].equals(selectedValue)) {
                writer.write(currentLine + System.getProperty("line.separator"));
            }
        }
        writer.close();
        reader.close();
        inputFile.delete();
        tempFile.renameTo(inputFile);
    } catch (IOException e) {
        e.printStackTrace();
    }
            JOptionPane.showMessageDialog(null, "ThankYou");
}


   
    
